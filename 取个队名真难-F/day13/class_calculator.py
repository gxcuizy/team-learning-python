class MyCalculator:

    def __init__(self):
        print("初始化")

    def add(self, x, y):
        # 相加

        return x + y

    def subtract(self, x, y):
        # 相减

        return x - y

    def multiply(self, x, y):
        # 相乘

        return x * y

    def divide(self, x, y):
        # 相除

        return x / y

    def output(self):
        print('肥仔别闹')
